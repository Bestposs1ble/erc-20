let provider;
let signer;
let tokenContract;

connectBtn.onclick = async () => {
  if (typeof window.ethereum === "undefined") {
    alert("请安装 MetaMask");
    return;
  }

  await window.ethereum.request({ method: "eth_requestAccounts" });

  provider = new ethers.BrowserProvider(window.ethereum);
  signer = await provider.getSigner();
  const address = await signer.getAddress();

  accountSpan.innerText = address;
  accountInfo.classList.remove("hidden");

  const ethBalance = await provider.getBalance(address);
  ethBalanceSpan.innerText = ethers.formatEther(ethBalance) + " ETH";

  tokenContract = new ethers.Contract(tokenAddress, tokenABI, signer);
  const tokenBalance = await tokenContract.balanceOf(address);
  const decimals = await tokenContract.decimals();

  tokenBalanceSpan.innerText = ethers.formatUnits(tokenBalance, decimals) + " BP";
};

transferBtn.onclick = async () => {
  const to = toAddressInput.value;
  const amount = amountInput.value;

  if (!to || !amount) {
    alert("请输入完整的收款地址和转账数量");
    return;
  }

  const decimals = await tokenContract.decimals();
  const value = ethers.parseUnits(amount, decimals);

  const tx = await tokenContract.transfer(to, value);
  await tx.wait();

  alert("✅ 转账成功！");
};
